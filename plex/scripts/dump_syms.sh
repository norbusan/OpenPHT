#!/bin/sh

DUMP_SYMS=$1
COMPRESS=$2
SOURCE=$3
TARGET=$4
DSYMSOURCE=$5

if [ -n "$DSYMSOURCE" ]; then
  echo $DUMP_SYMS -g "$DSYMSOURCE" "$SOURCE" | $COMPRESS > "$TARGET"
  $DUMP_SYMS -g "$DSYMSOURCE" "$SOURCE" | $COMPRESS > "$TARGET"
else
  echo $DUMP_SYMS "$SOURCE" | $COMPRESS > "$TARGET"
  $DUMP_SYMS "$SOURCE" | $COMPRESS > "$TARGET"
fi
